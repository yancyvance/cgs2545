-- Drop the database if it is currently existing
DROP DATABASE IF EXISTS grocery;
CREATE DATABASE grocery;
USE grocery;

-- Create the tables
CREATE TABLE Customer (
    CustomerID          INT,
    FirstName           VARCHAR(50),
    LastName            VARCHAR(50),
    StreetAddress       VARCHAR(100),
    City                VARCHAR(50),
    Zipcode             VARCHAR(10),
    State               CHAR(2),
    ContactNo           VARCHAR(20),
    Email               VARCHAR(100) NOT NULL UNIQUE,
CONSTRAINT pk_customer PRIMARY KEY (CustomerID)
);

CREATE TABLE Product (
    ProductID           INT,
    ProductName         VARCHAR(100) NOT NULL,
    UnitPrice           DECIMAL(10,2) NOT NULL,
    Category            VARCHAR(50) NOT NULL,
CONSTRAINT pk_product PRIMARY KEY (ProductID)
);

CREATE TABLE OnlineOrder (
    OrderID             INT,
    OrderDate           DATETIME NOT NULL,
    PaymentMethod       VARCHAR(20),
    ShipDate            DATETIME,
    OrderStatus         VARCHAR(20),
    CustomerID          INT NOT NULL,
CONSTRAINT pk_onlineorder PRIMARY KEY (OrderID),
CONSTRAINT fk_onlineorder_customer FOREIGN KEY (CustomerID)
    REFERENCES Customer(CustomerID)
);

CREATE TABLE OrderDetail (
    OrderDetailID       INT AUTO_INCREMENT,
    OrderID             INT NOT NULL,
    ProductID           INT NOT NULL,
    Qty                 INT NOT NULL,
CONSTRAINT pk_orderdetail PRIMARY KEY (OrderDetailID),
CONSTRAINT fk_orderdetail_order FOREIGN KEY (OrderID)
    REFERENCES OnlineOrder(OrderID),
CONSTRAINT fk_orderdetail_product FOREIGN KEY (ProductID)
    REFERENCES Product(ProductID)
);


